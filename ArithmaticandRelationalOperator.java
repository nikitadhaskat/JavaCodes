
public class ArithmaticandRelationalOperator {

	public static void main(String[] args)
	{
       int a=25;
       int b=25;
       System.out.println(a+b);//Arithmatic operator
       System.out.println(a-b);
       System.out.println(a*b);
       System.out.println(a/b);
       System.out.println(a%b);
       System.out.println(a<b);//Relational operator
       System.out.println(a<=b);
       System.out.println(a>b);
       System.out.println(a>=b);
       System.out.println(a==b);
       System.out.println(a!=b);
       

	}

}
